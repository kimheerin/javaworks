package data;

public class PrintData {

	public static void main(String[] args) {
		// 데이터 다루기
		// 숫자
		System.out.println(10); //정수
		System.out.println(2.54); //실수
		System.out.println(2.54+10); //덧셈
		
		
		// 문자 - 땅싸옴표로 감싼 데이터
		System.out.println("apple");
		System.out.println('A'); // 한 문자인 경우 홑따옴표 가능
	}

}
